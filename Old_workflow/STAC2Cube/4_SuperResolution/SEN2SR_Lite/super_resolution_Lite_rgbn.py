def print_summary(
    ds_sr: xr.Dataset
) -> None:
    """
    Print summary of super-resolved output cube.
    """

    print("\nSuper-resolved cube summary:")

    print(f"\nVariables: {list(ds_sr.data_vars)}")

    print(f"\nDimensions: {dict(ds_sr.sizes)}")

    print(f"\nEstimated size: {lazy_data_size(ds_sr)}")

    if "time" in ds_sr.coords:

        print("\nAvailable dates:")

        for value in ds_sr.time.values:
            print(f"  {value}")


