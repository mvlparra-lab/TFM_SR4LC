class Tee:
    """
    Write terminal output also into a report file.
    """

    def __init__(self, *streams: TextIO):
        self.streams = streams

    def write(self, data: str) -> int:
        for stream in self.streams:
            stream.write(data)
            stream.flush()

        return len(data)

    def flush(self) -> None:
        for stream in self.streams:
            stream.flush()


def ensure_parent(path_str: str | None) -> Path | None:
    """
    Create parent directory for a file path.
    """

    if path_str is None:
        return None

    path = Path(path_str)
    path.parent.mkdir(
        parents=True,
        exist_ok=True
    )

    return path


def infer_output_path(
    input_path: str,
    output_path: str | None
) -> str:
    """
    Infer output path if none is provided.
    """

    if output_path:
        return output_path

    input_file = Path(input_path)
    suffix = input_file.suffix or ".nc"

    return str(
        input_file.with_name(
            f"{input_file.stem}_sr{suffix}"
        )
    )


def is_stac_json(json_path: Path) -> bool:
    """
    Check if a JSON file looks like a valid STAC JSON.
    """

    try:
        with json_path.open(
            "r",
            encoding="utf-8"
        ) as file:
            obj = json.load(file)

    except Exception:
        return False

    if not isinstance(obj, dict):
        return False

    if "stac_version" not in obj:
        return False

    stac_type = obj.get("type")

    if stac_type in {
        "Feature",
        "Collection",
        "Catalog",
    }:
        return True

    if "assets" in obj or "links" in obj:
        return True

    return False


def resolve_rgbn_model_source(
    stac2cube_root: Path
) -> str:
    """
    Find local SEN2SRLite RGBN model STAC JSON.
    """

    candidate_dirs = [
        stac2cube_root / "interactive" / "model" / "SEN2SRLite_RGBN",
        stac2cube_root / "interactive" / "model" / "SEN2SRLite-RGBN",
        stac2cube_root / "model" / "SEN2SRLite_RGBN",
        stac2cube_root / "model" / "SEN2SRLite-RGBN",
    ]

    preferred_names = [
        "mlm.json",
        "item.json",
        "catalog.json",
        "collection.json",
        "model.json",
    ]

    for model_dir in candidate_dirs:

        if not model_dir.is_dir():
            continue

        print(f"\nSearching model directory: {model_dir}")

        json_files = sorted(
            model_dir.rglob("*.json")
        )

        for name in preferred_names:
            for json_file in json_files:

                if (
                    json_file.name.lower() == name
                    and is_stac_json(json_file)
                ):
                    print(f"Using model STAC JSON: {json_file}")
                    return str(json_file)

        for json_file in json_files:

            if is_stac_json(json_file):
                print(f"Using model STAC JSON: {json_file}")
                return str(json_file)

        raise FileNotFoundError(
            f"No valid STAC JSON found in model directory: {model_dir}"
        )

    searched = "\n".join(
        str(path)
        for path in candidate_dirs
    )

    raise FileNotFoundError(
        "Could not locate SEN2SRLite RGBN model directory. "
        "Searched:\n"
        + searched
    )


def patch_mlstac_load():
    """
    Patch mlstac.load to redirect RGBN model path.
    """

    original_load = mlstac.load

    model_root = (
        "/teamspace/studios/this_studio/"
        "STAC2Cube/4_SuperResolution/"
        "SEN2SR_Lite/model/"
        "SEN2SRLite_RGBN"
    )

    def patched_load(source, *args, **kwargs):

        source_str = str(source)

        if source_str == "model/SEN2SRLite-RGBN":

            source = model_root

            print(
                "\nRedirecting RGBN model:"
                f"\n  from: model/SEN2SRLite-RGBN"
                f"\n  to:   {source}"
            )

        return original_load(
            source,
            *args,
            **kwargs
        )

    mlstac.load = patched_load

    return original_load


def load_output_cube(
    output_path: str
) -> xr.Dataset:
    """
    Load super-resolved NetCDF output.
    """

    with xr.open_dataset(output_path) as ds:
        ds_loaded = ds.load()

    return ds_loaded


